/// <summary>
/// Memento Pattern
///
/// Intent: Without violating encapsulation, capture and externalize an object's internal state
/// so that the object can be restored to this state later.
///
/// When to use:
///   - When a snapshot of an object's state must be saved so it can be restored later
///   - When a direct interface to obtaining the state would expose implementation details
///
/// Real-world analogy: A game "save state" — you save your progress and can reload it later.
/// </summary>

