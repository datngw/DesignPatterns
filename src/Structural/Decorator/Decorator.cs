/// <summary>
/// Decorator Pattern
///
/// Intent: Attach additional responsibilities to an object dynamically.
/// Decorators provide a flexible alternative to subclassing for extending functionality.
///
/// When to use:
///   - To add responsibilities to individual objects dynamically, without affecting other objects
///   - When extension by subclassing is impractical (too many combinations)
///
/// Real-world analogy: Wearing layers of clothing — you add a sweater, jacket, or scarf
/// on top of your base outfit as needed.
